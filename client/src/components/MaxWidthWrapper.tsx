import { cn } from "@/lib/utils";
import { ReactNode } from "react";

{
  /* MaxWidthWrapper component is used to apply uniform style to all the child component*/
}
const MaxWidthWrapper = ({
  className,
  children,
}: {
  className?: string;
  children: ReactNode;
}) => {
  return (
    <div
      className={cn(
        "h-full mx-auto w-full max-w-screen-xl px-2.5 md:px-20",
        className
      )}
    >
      {children}
    </div>
  );
};

export default MaxWidthWrapper;
